package com.sanyinchen.parser.util;

/**
 * Created by sanyinchen on 19-3-23.
 * <p>
 * a template tuple class
 *
 * @param <K> class of first item
 * @param <T> class of second item
 * @author sanyinchen
 * @version v0.1
 * @since 19-3-23
 */
public class Tuple<K, T> {

    private K first;
    private T second;

    public Tuple(K first, T second) {
        this.first = first;
        this.second = second;
    }

    public K getFirst() {
        return first;
    }

    public void setFirst(K first) {
        this.first = first;
    }

    public T getSecond() {
        return second;
    }

    public void setSecond(T second) {
        this.second = second;
    }

}
